    private String range(int age) {
        String r;
        if (age == 0) {
            r = null;
        } else if (age < 14) {
            r = "ninios";
        } else if (age < 18) {
            r = "Jovenes";
        } else {
            r = "Adultos";
        }
        return r;
    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int rando = 0;
        do {

            rando = new Random().nextInt(tittles.size());
        } while (checkTitleUsed(rando, true));
        int id = 0;
        for(int i = 0 ; i < allTitles.size() ; i++){
            if(tittles.get(rando).equals(allTitles.get(i))){
                id = i;
                break;
            }
        }
        launchTest(id);

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int id = -1;
        boolean used = false;
        String u = "";
        do {
            if (used) {
                u = " - El anterior id ya fue usado con este usuario";
            }
            String sId = JOptionPane.showInputDialog("Inserte el ID de la prueba" + u);
            try {
                id = Integer.parseInt(sId);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Por favor solo numeros en el campo de IqD");
            }
        } while (used = checkTitleUsed(id - 1, false));
        launchTest(id-1);

    }//GEN-LAST:event_jButton2ActionPerformed

    private void launchTest(int id) {
        String[] answerWords = {};
        answerWords = ExcelConnection.getAnwerWords(allTitles.get(id));
        String[] answers = answerWords[1].split(",");
        int words = Integer.parseInt(answerWords[0]);
        new TestLauncher(answers, words, allTitles.get(id), idUser, id + 1).setVisible(true);
        dispose();

    }

    private boolean checkTitleUsed(int id, boolean r) {
        boolean used = false;
        this.r = r;
        String test = "";
        if (id >= 0) {
            if (r) {
                test = tittles.get(id);
            } else {
                test = allTitles.get(id);
            }
            for (String s : tests) {
                if (test.equals(s)) {
                    used = true;
                    break;
                }
            }
        }
        return used;
    }

