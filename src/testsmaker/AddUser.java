    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (ctf.comprobarTexto()) {
            String name = jTextField1.getText();
            String lastName = jTextField2.getText();
            String ageS = jTextField3.getText();
            try {
                int age = Integer.parseInt(ageS);
                ExcelConnection.addUser(name, lastName, age);
                JOptionPane.showMessageDialog(null,"Usuario agregado con Ã©xito");
                
            } catch (NumberFormatException | SQLException e) {
                jTextField3.setBackground(Color.RED);
                JOptionPane.showMessageDialog(null,"En la casilla de edad solo son permitidos numeros");
                
            }finally{
                ctf.todoInicial();
            }
            
        }else{
            JOptionPane.showMessageDialog(null,"Llene todas las casillas por favor");
            ctf.todoInicial();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

